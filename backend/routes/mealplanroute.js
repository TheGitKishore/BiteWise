const normalizePlanType = (value) => {
  const key = String(value || '').trim().toLowerCase();
  return PLAN_TYPE_ALIASES[key] || 'Balanced Diet';
};

const MEAL_KEYS = ['breakfast', 'lunch', 'dinner', 'snack'];

const sanitizeMealText = (value) => String(value || '').trim();

const sanitizeDays = (days, numDays = 7) => {
  const source = Array.isArray(days)
    ? days
    : Array.from({ length: Number(numDays) || 7 }, (_, i) => ({ day: i + 1 }));

  return source.map((dayObj, idx) => {
    const clean = { day: Number(dayObj?.day) || idx + 1 };

    for (const key of MEAL_KEYS) {
      const text = sanitizeMealText(dayObj?.[key]);
      if (text.length > 0) clean[key] = text;
    }

    return clean;
  });
};

const shuffle = (arr) => {
  const copy = [...arr];
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
};

