router.put('/:planId', async (req, res) => {
  try {
    const { planId } = req.params;
    const { name, description, numDays, days, planType, isAutoGenerated } = req.body;

    if (!ObjectId.isValid(planId)) {
      return res.status(400).json({ success: false, field: null, message: 'Invalid plan id.', data: null });
    }

    const update = { updatedAt: new Date().toISOString() };

    if (typeof name !== 'undefined') {
      if (!String(name).trim()) {
        return res.status(400).json({ success: false, field: 'name', message: 'Plan name is required.', data: null });
      }
      update.name = String(name).trim();
    }

    if (typeof description !== 'undefined') {
      update.description = String(description || '').trim();
    }

    if (typeof numDays !== 'undefined') {
      update.numDays = Number(numDays) || 7;
    }

    if (typeof days !== 'undefined') {
      update.days = sanitizeDays(days, Number(update.numDays || numDays || 7));
    }

    if (typeof planType !== 'undefined') {
      update.planType = normalizePlanType(planType);
    }

    if (typeof isAutoGenerated !== 'undefined') {
      update.isAutoGenerated = Boolean(isAutoGenerated);
    }

    const db = getDB();
    const collection = db.collection('meal_plans');

    const result = await collection.updateOne(
      { _id: new ObjectId(planId), isDeleted: { $ne: true } },
      { $set: update }
    );

    if (result.matchedCount === 0) {
      return res.status(404).json({ success: false, field: null, message: 'Meal plan not found.', data: null });
    }

    const updatedPlan = await collection.findOne({ _id: new ObjectId(planId) });
    if (!updatedPlan) {
      return res.status(404).json({ success: false, field: null, message: 'Meal plan not found.', data: null });
    }

    return res.json({
      success: true,
      field: null,
      message: 'Meal plan updated successfully!',
      data: toClientPlan(updatedPlan),
    });
  } catch (err) {
    return res.status(500).json({ success: false, field: null, message: err.message, data: null });
  }
});

router.delete('/:planId', async (req, res) => {
  try {
    const { planId } = req.params;

    if (!ObjectId.isValid(planId)) {
      return res.status(400).json({ success: false, message: 'Invalid plan id.' });
    }

    const db = getDB();
    const collection = db.collection('meal_plans');

    const result = await collection.updateOne(
      { _id: new ObjectId(planId), isDeleted: { $ne: true } },
      {
        $set: {
          isDeleted: true,
          deletedAt: new Date().toISOString(),
          updatedAt: new Date().toISOString(),
        },
      }
    );

    if (result.matchedCount === 0) {
      return res.status(404).json({ success: false, message: 'Meal plan not found.' });
    }

    return res.json({ success: true, message: 'Meal plan deleted' });
  } catch (err) {
    return res.status(500).json({ success: false, message: err.message });
  }
});

