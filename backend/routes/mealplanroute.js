const pickForDay = (pool, usedInWeek) => {
  const shuffled = shuffle(pool);
  const pick = shuffled.find((item) => !usedInWeek.has(item)) || shuffled[0] || '';
  if (pick) usedInWeek.add(pick);
  return pick;
};

const buildGeneratedDays = (planType, dayCount = 7) => {
  const category = HARD_CODED_TEMPLATES[planType] || HARD_CODED_TEMPLATES['Balanced Diet'];

  const used = {
    breakfast: new Set(),
    lunch: new Set(),
    dinner: new Set(),
    snack: new Set(),
  };

  return Array.from({ length: dayCount }, (_, i) => ({
    day: i + 1,
    breakfast: pickForDay(category.breakfast, used.breakfast),
    lunch: pickForDay(category.lunch, used.lunch),
    dinner: pickForDay(category.dinner, used.dinner),
    snack: pickForDay(category.snack, used.snack),
  }));
};

const toClientPlan = (doc) => ({
  planId: doc._id.toString(),
  userId: Number(doc.userId),
  name: doc.name || '',
  description: doc.description || '',
  numDays: Number(doc.numDays) || 7,
  days: Array.isArray(doc.days) ? doc.days : [],
  isAutoGenerated: Boolean(doc.isAutoGenerated),
  planType: doc.planType || '',
  createdAt: doc.createdAt,
  updatedAt: doc.updatedAt || null,
});

router.get('/:userId', async (req, res) => {
  try {
    const userId = Number(req.params.userId);
    if (!userId) {
      return res.status(400).json({ success: false, data: [], message: 'Invalid user id.' });
    }

    const db = getDB();
    const collection = db.collection('meal_plans');

    const plans = await collection
      .find({ userId, isDeleted: { $ne: true } })
      .sort({ createdAt: -1 })
      .toArray();

    return res.json({
      success: true,
      data: plans.map(toClientPlan),
      message: '',
    });
  } catch (err) {
    return res.status(500).json({ success: false, data: [], message: err.message });
  }
});

router.post('/', async (req, res) => {
  try {
    const { userId, name, description, numDays, days } = req.body;

    if (!name || !String(name).trim()) {
      return res.status(400).json({
        success: false,
        field: 'name',
        message: 'Plan name is required.',
        data: null,
      });
    }

    const resolvedNumDays = Number(numDays) || 7;
    const sanitizedDays = sanitizeDays(days, resolvedNumDays);

    const doc = {
      userId: Number(userId),
      name: String(name).trim(),
      description: String(description || '').trim(),
      numDays: resolvedNumDays,
      days: sanitizedDays,
      isAutoGenerated: false,
      planType: '',
      isDeleted: false,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    };

    const db = getDB();
    const collection = db.collection('meal_plans');
    const result = await collection.insertOne(doc);

    return res.status(201).json({
      success: true,
      field: null,
      message: 'Meal plan created successfully!',
      data: toClientPlan({ ...doc, _id: result.insertedId }),
    });
  } catch (err) {
    return res.status(500).json({
      success: false,
      field: null,
      message: err.message,
      data: null,
    });
  }
});

