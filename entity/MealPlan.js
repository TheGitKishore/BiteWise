  // Returns a "Sample Day 1:" preview string for the plan card
  getSampleDayPreview() {
    if (!this.days || this.days.length === 0) return null;
    const day   = this.days[0];
    const meals = [day.breakfast, day.lunch, day.dinner].filter(Boolean);
    return meals.length > 0
      ? `Sample Day 1:\n${meals.map((m) => `• ${m}`).join('\n')}`
      : 'Sample Day 1:';
  }


  // STATIC VALIDATION METHODS

  // UC #26, #69 — validate meal plan creation
  // @param  {{ name }}
  // @return {{ valid: boolean, field: string|null, message: string }}
  static validatePlan({ name }) {
    if (!name || name.trim().length === 0) {
      return { valid: false, field: 'name', message: 'Plan name is required.' };
    }
    return { valid: true, field: null, message: '' };
  }


