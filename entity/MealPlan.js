  // UC #26, #69 — create a custom meal plan
  // @param  {number} userId
  // @param  {{ name, description, numDays, days }}
  // @return {Promise<{ success, field, message, data }>}
  static async create(userId, { name, description, numDays, days }) {
    const check = MealPlan.validatePlan({ name });
    if (!check.valid) {
      return { success: false, field: check.field, message: check.message, data: null };
    }

    const resolvedDays = days && days.length > 0
      ? days
      : Array.from({ length: numDays || 7 }, (_, i) => ({
          day: i + 1, breakfast: '', lunch: '', dinner: '', snack: '',
        }));

    const plan = new MealPlan({
      planId:          Date.now(),
      userId,
      name:            name.trim(),
      description:     description || '',
      numDays:         numDays || 7,
      days:            resolvedDays,
      isAutoGenerated: false,
      createdAt:       new Date().toISOString(),
    });

    return {
      success: true,
      field:   null,
      message: 'Meal plan created successfully!',
      data:    plan,
    };
  }

